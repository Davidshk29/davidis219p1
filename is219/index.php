			<h2 align="center">Spring</h2>
			<p>Camp Moore</p>
			Many children go to summer camp every year and don’t realize how important it is for a child’s psyche
			and social development. Many of us have some of funniest memories from summer camp or a friend we would
			not have met had it not been for that camp.  The Elks association, particularly those in New Jersey,
			have pooled together resources to create a safe and inclusive weeklong summer camp for children with 
			special needs: Camp Moore.
			Unbeknownst to many, this would be the second iteration of Camp Moore. The first Camp Moore, which was 
			named after the former Governor, was established in the 1930s under similar premises as its contemporary
			but was close to shutting down due to financial difficulties. The Elks were, at the time, searching for
			camp. One thing lead to another and the current iteration of the camp was born in the 1970s and it has
			been running every summer since. One our brothers is part of an Elk’s Lodge and offered the active
			brotherhood a chance to support this amazing camp several years ago. Without second thought, Kappa Xi 
			Kappa has been supporting the camp every since with our weekly informational events and dinner. 

