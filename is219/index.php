			<p>Movember</p>
			As many might know, the Movember Foundation started the trend of “No Shave November” to bring
			awareness to men’s health. The origin of the word Movember dates back to a group in South Australia 
			growing out their moustaches for charity. Grabbing airtime on a local news network, the movement 
			started from a group of about 80 men to a national phenomenon. Several years later, an unrelated 
			group in the Australian state of Victoria where 30 men would grow out their facial hair for 30 days 
			to raise awareness. This second group of 30 men would later become the Movember Foundation Charity. 
			Since its inception, the Movember foundation has raised nearly 200 million worldwide for the purposes
			of helping enrich and enhance men’s health. While originally just for prostate cancer and mental health
			in men, The Movember Foundation absorbed the testicular cancer fundraiser Tacheback. We at Kappa Xi
			Kappa are proud to say that we have supported Movember evey fall for the past seven years. In the fall
			2018 semester we raised over 2,000 dollars for men’s health with our campaigns of tabling on Wednesdays
